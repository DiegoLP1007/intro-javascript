// == : igualdad
// === : para comparar igualdad en valor y en tipo de dato
//!= : desigualdad
// !== : es diferente en valor y en tipo de dato

// <: menor que
//>: mayor que
// <=: menor e igual que
// =>: mayor o igual que

const a = 10;
const b = 20;
const c = '10'

console.log(a == b) // false
console.log( a === c)//false
console.log( a == c)// true
console.log(a != c)//falso -> si se interpreta como iguales
console.log(a !== c)// true 




